// In JavaScript, the join() method is used to join all elements 
// of an array into a single string and return the resulting string.

const fruits = ['Apple', 'Banana', 'Orange'];

// Join elements with default separator (comma)
const result1 = fruits.join(); // 
// console.log(result1);

// Join elements with a custom separator (space)
const result2 = fruits.join(' ');
// console.log(result2);

// Join elements with a custom separator (hyphen)
const result3 = fruits.join('-');
// console.log(result3);
